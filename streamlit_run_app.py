# ДОПОЛНЕНИЕ ДЛЯ ШАГА 3: ЛОГИКА СЕМАНТИЧЕСКИХ ГРАФОВ
# ==========================================
def render_semantic_graph(country):
    """
    Генерирует семантическую карту дисциплины (граф).
    Узлы (инвариант) остаются одинаковыми, а связи и визуализация (адаптивность) 
    меняются в зависимости от культурного профиля (Глава 5).
    """
    nodes = []
    edges = []

    # 1. ИНВАРИАНТ: Базовые узлы (глоссарий) одинаковы для всех
    nodes.append(Node(id="Knowledge", label="Объект знания", size=25, color="#4A90E2"))
    nodes.append(Node(id="Theory", label="Теоретический базис", size=15))
    nodes.append(Node(id="Practice", label="Практический кейс", size=15))
    nodes.append(Node(id="Context", label="Социокультурный контекст", size=15))
    nodes.append(Node(id="Authority", label="Авторитетное мнение", size=15))
    nodes.append(Node(id="Conclusion", label="Вывод / Инновация", size=20, color="#50E3C2"))

    # 2. АДАПТИВНОСТЬ: Связи и структура зависят от страны
    if country == "США":
        # Аналитический стиль: Линейно, от фактов к выводу, игнорируем "контекст" и "авторитет"
        edges.append(Edge(source="Knowledge", target="Theory", label="основано на"))
        edges.append(Edge(source="Theory", target="Practice", label="применяется в"))
        edges.append(Edge(source="Practice", target="Conclusion", label="формирует"))
        
        # Конфигурация: Иерархическое дерево (сверху вниз)
        config = Config(
            width=700, height=400, directed=True, 
            physics=False, hierarchical=True, # Жесткая структура
            nodeHighlightBehavior=True, highlightColor="#F7A072"
        )
        st.info("💡 **Навигация:** Линейная (дедуктивная). Внимание сфокусировано на логической цепочке от фактов к выводу. Авторитет и контекст выведены на периферию.")

    elif country == "Китай":
        # Холистический стиль: Всё связано со всем, фокус на контекст и традицию (авторитет)
        edges.append(Edge(source="Authority", target="Knowledge", label="определяет"))
        edges.append(Edge(source="Context", target="Knowledge", label="обрамляет"))
        edges.append(Edge(source="Context", target="Theory", label="влияет на"))
        edges.append(Edge(source="Theory", target="Practice", label="синтез"))
        edges.append(Edge(source="Practice", target="Conclusion", label="интерпретация"))
        edges.append(Edge(source="Authority", target="Conclusion", label="подтверждает"))
        
        # Конфигурация: Круговая паутина (всё связано, физика включена)
        config = Config(
            width=700, height=400, directed=True, 
            physics=True, hierarchical=False, # Свободная сеть
            nodeHighlightBehavior=True, highlightColor="#F7A072"
        )
        st.info("💡 **Навигация:** Холистическая. Знание не отделимо от традиций (авторитета) и среды (контекста). Круговая структура связей.")

    elif country == "ОАЭ":
        # Нарративный стиль: Авторитет задает теорию, теория применяется в практике
        edges.append(Edge(source="Authority", target="Theory", label="задает"))
        edges.append(Edge(source="Authority", target="Context", label="регулирует"))
        edges.append(Edge(source="Context", target="Practice", label="условия"))
        edges.append(Edge(source="Theory", target="Knowledge", label="раскрывает"))
        edges.append(Edge(source="Knowledge", target="Conclusion", label="усвоение"))

        # Конфигурация: Сеть
        config = Config(
            width=700, height=400, directed=True, 
            physics=True, hierarchical=False, 
            nodeHighlightBehavior=True, highlightColor="#F7A072"
        )
        st.info("💡 **Навигация:** Нарративно-дедуктивная. Авторитетные источники формируют контекст, внутри которого изучается объект.")

    # Отрисовка графа в Streamlit
    return agraph(nodes=nodes, edges=edges, config=config)

