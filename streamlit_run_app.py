# ШАГ 2: ЛОГИКА (LOGIC LAYER) - ДВИЖОК ИОС
# ==========================================
class AdaptiveEngine:
    def __init__(self, database):
        self.db = database

    def build_student_model(self, country):
        """Возвращает полный профиль студента на основе выбранного этно-кейса"""
        return self.db.get(country, None)

