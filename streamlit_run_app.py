import streamlit as st

# Класс Экспертной системы (Логика управления по Таратухиной)
class CrossCulturalAdaptiveSystem:
    def __init__(self):
        # 1. Базовая дидактическая логика (Глава 3 автореферата)
        self.rules = {
            "culture_type": {
                "Коллективистская (Восток)": {
                    "role": "Преподаватель-Гуру (директивный стиль)",
                    "content": "Нарративный контент, обилие прецедентов, видео-лекции",
                    "interface": "Иерархическая структура, теплые тона, обилие визуальных подсказок",
                    "methods": "Рецептивные и репродуктивные методы",
                    "assessment": "Тесты на воспроизведение знаний, коллективные оценки"
                },
                "Индивидуалистическая (Запад)": {
                    "role": "Преподаватель-Коуч (содействующий стиль)",
                    "content": "Атомарный контент, аналитические таблицы, ссылки на источники",
                    "interface": "Минималистичный дизайн, линейная навигация, кастомизация",
                    "methods": "Эвристические и проблемно-поисковые методы",
                    "assessment": "Эссе, дебаты, оценка индивидуального прогресса"
                }
            },
            "cognitive_style": {
                "Полезависимый (Холистический)": "Холистическая подача (от общего к частному), ментальные карты",
                "Поленезависимый (Аналитический)": "Аналитическая подача (детализация), структурные схемы"
            }
        }

        # 2. Этно-дизайнерская логика (Глава 4 автореферата)
        self.ethno_ui_rules = {
            "Великобритания": {
                "layout": "Линейный, минималистичный (Low Power Distance)",
                "navigation": "Свободная, центрированная на студенте",
                "colors": "Сдержанная бизнес-палитра (синий, серый)",
                "feedback": "Прямая, критическая, поощряющая дискуссию"
            },
            "Китай": {
                "layout": "Иерархический, комплексный (High Power Distance)",
                "navigation": "Строго регламентированная, пошаговая",
                "colors": "Теплые тона, красный, золотой (традиционная палитра)",
                "feedback": "Мягкая, сохраняющая 'лицо' студента"
            },
            "Арабские страны": {
                "layout": "Зеркальный (RTL - справа налево), высококонтекстный",
                "navigation": "Образно-нарративная, через кейсы",
                "colors": "Зеленый, песочный, использование орнаменталистики",
                "feedback": "Опосредованная, с учетом статуса иерархии"
            }
        }

    def get_recommendation(self, culture, cog_style, country):
        # Получаем данные из словарей по ключам
        res = self.rules["culture_type"][culture]
        cog = self.rules["cognitive_style"][cog_style]
        ethno = self.ethno_ui_rules[country]
        return res, cog, ethno

# UI приложения на Streamlit
def main():
    st.set_page_config(page_title="Personalized IEE System", layout="wide")
    
    st.title("Система управления обучением в поликультурной ИОС")
    st.subheader("Рекомендательный сервис персонализации образовательной траектории")

    # Боковая панель ввода данных
    with st.sidebar:
        st.header("Входные параметры профиля")
        
        culture = st.selectbox("Культурно-когнитивный тип:", 
                               ["Коллективистская (Восток)", "Индивидуалистическая (Запад)"])
        
        cog_style = st.selectbox("Когнитивный стиль:", 
                                 ["Полезависимый (Холистический)", "Поленезависимый (Аналитический)"])
        
        selected_country = st.selectbox("Культурный регион (этно-кейс):", 
                                        ["Великобритания", "Китай", "Арабские страны"])
        
        st.divider()
        st.info("Алгоритм базируется на принципах кросс-культурной дидактики Ю.В. Таратухиной.")

    # Создаем экземпляр экспертной системы
    expert_system = CrossCulturalAdaptiveSystem()
    
    # Получаем рекомендации
    rec, cog_rec, ethno_rec = expert_system.get_recommendation(culture, cog_style, selected_country)

    # Вывод результатов: Блок 1 и 2
    col1, col2 = st.columns(2)

    with col1:
        st.success("### 1. Архитектура интерфейса и контента")
        st.write(f"**Тип интерфейса:** {rec['interface']}")
        st.write(f"**Организация знаний:** {cog_rec}")
        st.write(f"**Формат контента:** {rec['content']}")

    with col2:
        st.warning("### 2. Методическая стратегия")
        st.write(f"**Роль педагога:** {rec['role']}")
        st.write(f"**Доминирующие методы:** {rec['methods']}")
        st.write(f"**Формат КИМ:** {rec['assessment']}")

    # Вывод результатов: Блок 3 (Этно-дизайн)
    st.divider()
    st.info(f"### 3. Визуальный паспорт интерфейса ({selected_country})")
    
    c1, c2 = st.columns(2)
    with c1:
        st.write(f"**Архитектура экрана:** {ethno_rec['layout']}")
        st.write(f"**Логика навигации:** {ethno_rec['navigation']}")
    with c2:
        st.write(f"**Цветовое решение:** {ethno_rec['colors']}")
        st.write(f"**Стиль обратной связи:** {ethno_rec['feedback']}")

    st.divider()
    st.caption("Программный модуль разработан для верификации положений докторской диссертации Таратухиной Ю.В. (2026)")

if __name__ == "__main__":
    main()